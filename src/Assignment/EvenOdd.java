package Assignment;

public class EvenOdd {
	public static void main(String[] args) {
		for(int i=1;i<=100;i++) {
			if(i%2==0) {
				//System.out.println(i + " ");
				System.out.println("This is Even No...." +i);
			}
				
			if(i%2!=0) {
				System.out.println("This is Odd No...." +i);

			}
		}
	}
}
	


