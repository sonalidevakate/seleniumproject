package BasicProgramming;

public class Demo {
	public static void main(String[] args) {
		//Declaration
		String name;
		int rollno;
		char grade;
		float percentage;
		
		//Initialization
		name="Sonali";
		rollno=05;
		grade='A';
		percentage=80.66f;
		
		//Usage
		System.out.println("My name is:" + name); 
		System.out.println("My rollno is:" + rollno); 
		System.out.println("My grade is:" + grade);
		System.out.println("My percentage is:" + percentage + "%"); 
	}

}
